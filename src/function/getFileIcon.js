export default function (language){
    switch(language){
        case 'javascript':
            return "https://upload.wikimedia.org/wikipedia/commons/thumb/9/99/Unofficial_JavaScript_logo_2.svg/2048px-Unofficial_JavaScript_logo_2.svg.png";
        case 'python':
            return "https://upload.wikimedia.org/wikipedia/commons/thumb/c/c3/Python-logo-notext.svg/1869px-Python-logo-notext.svg.png";
        case 'java':
            return "https://logoeps.com/wp-content/uploads/2011/06/java-logo-vector.png";
        case 'c':
            return "https://upload.wikimedia.org/wikipedia/commons/thumb/1/18/C_Programming_Language.svg/695px-C_Programming_Language.svg.png";
        case 'cpp':
            return "https://upload.wikimedia.org/wikipedia/commons/thumb/1/18/C_Programming_Language.svg/695px-C_Programming_Language.svg.png";
    }
}